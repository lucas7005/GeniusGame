package tela;


public class Player {
	

	private String nome;

	private int pontos;
	
	
	
	public String getNome() {
		return nome;
	}
	public int getPontos() {
		return pontos;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setPontos(int pontos) {
		this.pontos = pontos;
	}
	
	

}
